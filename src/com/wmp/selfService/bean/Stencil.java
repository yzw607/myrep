package com.wmp.selfService.bean;

public class Stencil
{
    private int stencilId;
    private String stencilName;
    private String screenType;
    private String color;
    private String style;
    private String subject;
    private String location;
    private String splitScreen;
    private String directions;

    public int getStencilId()
    {
        return stencilId;
    }

    public void setStencilId(int stencilId)
    {
        this.stencilId = stencilId;
    }

    public String getStencilName()
    {
        return stencilName;
    }

    public void setStencilName(String stencilName)
    {
        this.stencilName = stencilName;
    }

    public String getScreenType()
    {
        return screenType;
    }

    public void setScreenType(String screenType)
    {
        this.screenType = screenType;
    }

    public String getColor()
    {
        return color;
    }

    public void setColor(String color)
    {
        this.color = color;
    }

    public String getStyle()
    {
        return style;
    }

    public void setStyle(String style)
    {
        this.style = style;
    }

    public String getSubject()
    {
        return subject;
    }

    public void setSubject(String subject)
    {
        this.subject = subject;
    }

    public String getLocation()
    {
        return location;
    }

    public void setLocation(String location)
    {
        this.location = location;
    }

    public String getSplitScreen()
    {
        return splitScreen;
    }

    public void setSplitScreen(String splitScreen)
    {
        this.splitScreen = splitScreen;
    }

    public String getDirections()
    {
        return directions;
    }

    public void setDirections(String directions)
    {
        this.directions = directions;
    }

}
