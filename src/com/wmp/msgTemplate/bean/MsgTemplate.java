package com.wmp.msgTemplate.bean;

public class MsgTemplate {
	private int tmpltId;
	private String tmpltTitle;
	private String tmpltContent;
	private String tmpltComment;
	public int getTmpltId() {
		return tmpltId;
	}
	public void setTmpltId(int tmpltId) {
		this.tmpltId = tmpltId;
	}
	public String getTmpltTitle() {
		return tmpltTitle;
	}
	public void setTmpltTitle(String tmpltTitle) {
		this.tmpltTitle = tmpltTitle;
	}
	public String getTmpltContent() {
		return tmpltContent;
	}
	public void setTmpltContent(String tmpltContent) {
		this.tmpltContent = tmpltContent;
	}
	public String getTmpltComment() {
		return tmpltComment;
	}
	public void setTmpltComment(String tmpltComment) {
		this.tmpltComment = tmpltComment;
	}

}
